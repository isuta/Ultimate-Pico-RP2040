# メモリ管理設定
# ----------------------------------------------------------------
# シナリオ完了時にガーベージコレクションを実行（推奨: True）
# シナリオ実行後の待ち時間を利用してメモリを整理するため、ユーザー体験に影響なし
GC_ON_SCENARIO_COMPLETE = True

# ガーベージコレクションを実行する間隔（メインループの反復回数）
# 定期的にGCを実行（フェイルセーフ/デバッグ用）
# 設定値:
#   0     = 無効（推奨: シナリオ完了時GCで十分）
#   1000  = 約50秒ごと
#   2000  = 約100秒ごと
#   4000  = 約200秒ごと
# 
# 【理由】待機中（シナリオ未実行）はメモリ断片化がほとんど発生しないため、
# シナリオ完了時GCのみで十分。定期GCは以下の場合のみ有効:
#   - 予期しないメモリリークのデバッグ
#   - メインループ処理のメモリ使用量を監視
#   - 極めて慎重な運用（24時間稼働等）
# 通常運用では 0（無効）を推奨
GC_INTERVAL = 0

# メモリ使用量のログ出力を有効化（デバッグ用、通常はFalse推奨）
GC_MEMORY_LOGGING = False

# ログ設定
# ----------------------------------------------------------------
# ログ出力レベル
# 0: ERROR のみ（本番環境、エラーのみ記録）
# 1: WARNING + ERROR（本番環境推奨、警告と重大エラー）
# 2: INFO + WARNING + ERROR（通常動作、主要な動作状況を表示）
# 3: DEBUG（開発・デバッグ用、全ての詳細情報を表示）
