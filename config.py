# =================================================================
# ハードウェア設定ファイル
# -----------------------------------------------------------------
# コンポーネント       | GPIOピン | 役割
# -----------------------------------------------------------------
# DFPlayer Mini        | GP12     | UART送信 (TX)
#                      | GP13     | UART受信 (RX)
#                      | GP14     | BUSY信号 (入力)
# -----------------------------------------------------------------
# OLEDディスプレイ     | GP16     | I2Cデータ (SDA)
#                      | GP17     | I2Cクロック (SCL)
# -----------------------------------------------------------------
# タクトスイッチ       | GP18     | ボタン入力 (プルダウン抵抗)
# -----------------------------------------------------------------
# NeoPixel (LV1-LV4) | GP20-23 | NeoPixel DATA
# -----------------------------------------------------------------
# ポテンショメータ     | GP26     | ボリューム (ADC入力)
# -----------------------------------------------------------------

# UART設定
UART_ID = 0
UART_BAUDRATE = 9600
DFPLAYER_TX_PIN = 12
DFPLAYER_RX_PIN = 13
DFPLAYER_BUSY_PIN = 14
DFPLAYER_DEFAULT_VOLUME = 10  # デフォルト音量 (0-30)

# I2C (OLEDディスプレイ用)
I2C_ID = 0
OLED_SDA_PIN = 16
OLED_SCL_PIN = 17
I2C_FREQ = 400000

# OLEDのサイズ設定
OLED_WIDTH = 128
OLED_HEIGHT = 64

# ボタン設定
BUTTON_PIN = 18

# 内蔵LED設定（Raspberry Pi Pico 2W）
# 内蔵LEDはGP25に接続されている（Pico 2Wの場合）
ONBOARD_LED_PIN = "LED"  # PicoのAPIでは"LED"で内蔵LEDにアクセス可能

# NeoPixel設定
# 論理名とピン番号・LED数を紐づけ
# LED数は今後変更
NEOPIXEL_STRIPS = {
    # 'ストリップ名': {'pin': ピン番号, 'count': LEDの数}
    'LV1': {'pin': 20, 'count': 15},
    'LV2': {'pin': 21, 'count': 15},
    'LV3': {'pin': 22, 'count': 15},
    'LV4': {'pin': 23, 'count': 15},
}

# ポテンショメータ / ADC設定
# ----------------------------------------------------------------
# GPIO 26はボリュームコントロール用ポテンショメータに接続
POTENTIOMETER_PIN = 26
# DFPlayerのボリュームは0から30まで
DFPLAYER_MAX_VOLUME = 30
# ADCの読み取り値が変わったとみなすデッドゾーン。
# 0-65535の範囲で500の変動は比較的少ないノイズとして処理されます。
VOLUME_DEADZONE = 500

# システム / タイミング設定
# ----------------------------------------------------------------
# ボタン入力やADCポーリングの際のメインループの待機時間 (ms)
MAIN_LOOP_POLLING_MS = 50
# 操作がない場合にアイドル状態（例: OLED消灯、LED全オフ）に移行するまでの時間 (ms)
# 300000ms = 5分
IDLE_TIMEOUT_MS = 300000
